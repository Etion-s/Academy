// 다차원 배열의 길이값 예상해보기
package Part5;

public class part5_6 {
    public static void main(String[] args) {
        int[][] array = {
                {34,34},
                {34,34,34},
                {23,23,23,23,23}
        };
        System.out.println(array.length); //3
        System.out.println(array[2].length); //5
    }


}
